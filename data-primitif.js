// gunakan number / angka agar nilai variable dapat dijumlahkan atau dikurangi
const nilaiUjian = 99;

console.log(nilaiUjian);

// gunakan boolean utk variabel yang memiliki 2 kebenaran
const apakahBumiBenarBulat = true;

console.log(apakahBumiBenarBulat);

// Tipe data null ini mewakili kekosongan. Makanya, tipe data ini biasa disebut “data yang tidak ada”.
const bukuKosong = null;
console.log(bukuKosong);

// Sesuai dengan namanya, tipe data ini adalah tipe data yang tidak terdefinisi. Artinya, tidak ada sumber nilai pada data tersebut.
const bukuGatau = undefined;
console.log(bukuGatau);